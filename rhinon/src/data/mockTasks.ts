// Helper to get task by ID
export const getTaskById = (id: string): Task | undefined => {
    return mockTasks.find((task) => task.id === id);
};

// Helper to get tasks by status
export const getTasksByStatus = (status: Task["status"]): Task[] => {
    return mockTasks.filter((task) => task.status === status);
};

// Helper to get tasks by assignee
export const getTasksByAssignee = (assigneeId: string): Task[] => {
    return mockTasks.filter((task) => task.assignee?.id === assigneeId);
};
